namespace Admin\Model;

use Zend\InputFilter\InputFilter;
use Zend\InputFilter\Factory as InputFactory;
use Zend\InputFilter\InputFilterAwareInterface;
use Zend\InputFilter\InputFilterInterface;

class Aclrole implements InputFilterAwareInterface
{
	public $aclroles_id;
	public $roleslug;
	public $rolename;

	protected $inputFilter;
	protected $userService;
	protected $serviceManager;
	protected $serviceLocator;
	
	public function exchangeArray($data)
	{
		$this->aclroles_id	= (isset($data['aclroles_id'])) ? $data['aclroles_id'] : null;
		$this->roleslug		= (isset($data['roleslug'])) ? $data['roleslug'] : null;
		$this->rolename		= (isset($data['rolename'])) ? $data['rolename'] : null;
	}

	public function getArrayCopy()
	{
		return get_object_vars($this);
	}
	
	public function setInputFilter(InputFilterInterface $inputFilter)
	{
		$this->inputFilter = $inputFilter;
		//throw new \Exception("Not used");
	}

	public function getInputFilter()
	{
		if (!$this->inputFilter) {
			$inputFilter = new InputFilter();
			$factory	 = new InputFactory();

			$inputFilter->add($factory->createInput(array(
				'name'	 => 'aclroles_id',
				'required' => true,
				'filters'  => array(
					array('name' => 'Int'),
				),
			)));

			$inputFilter->add($factory->createInput(array(
				'name'	 => 'roleslug',
				'required' => true,
				'filters'  => array(
					array('name' => 'StripTags'),
					array('name' => 'StringTrim'),
				),
				'validators' => array(
					array(
						'name'	=> 'StringLength',
						'options' => array(
							'encoding' => 'UTF-8',
							'min'	  => 3,
							'max'	  => 255,
						),
					),
				),
			)));

			$inputFilter->add($factory->createInput(array(
				'name'	 => 'rolename',
				'required' => true,
				'filters'  => array(
					array('name' => 'StripTags'),
					array('name' => 'StringTrim'),
				),
				'validators' => array(
					array(
						'name'	=> 'StringLength',
						'options' => array(
							'encoding' => 'UTF-8',
							'min'	  => 1,
							'max'	  => 255,
						),
					),
				),
			)));

			$this->inputFilter = $inputFilter;
		}

		return $this->inputFilter;
	}
	/**
	 * Getters/setters for DI stuff
	 */

	public function getUserService()
	{
		if (!$this->userService) {
			$this->userService = $this->getServiceManager()->get('zfcuser_user_service');
		}
		return $this->userService;
	}

	public function setUserService(UserService $userService)
	{
		$this->userService = $userService;
		return $this;
	}

	/**
	 * Retrieve service manager instance
	 *
	 * @return ServiceManager 
	 */
	public function getServiceManager()
	{
		return $this->serviceManager;
	}

	/**
	 * Set service manager instance
	 *
	 * @param ServiceManager $serviceManager
	 * @return Acl
	 */
	public function setServiceManager(ServiceManager $serviceManager)
	{
		$this->serviceManager = $serviceManager;
		return $this;
	}

	/**
	 * Set serviceManager instance
	 *
	 * @param  ServiceLocatorInterface $serviceLocator
	 * @return void
	 */
	public function setServiceLocator(ServiceLocatorInterface $serviceLocator)
	{
		$this->serviceLocator = $serviceLocator;
	}

	/**
	 * Retrieve serviceManager instance
	 *
	 * @return ServiceLocatorInterface
	 */
	public function getServiceLocator()
	{
		return $this->serviceLocator;
	}

}